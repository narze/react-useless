export function use1337(initialValue: string) {
  return initialValue
    .toUpperCase()
    .replaceAll("A", "4")
    .replaceAll("B", "8")
    .replaceAll("E", "3")
    .replaceAll("G", "6")
    .replaceAll("I", "1")
    .replaceAll("L", "1")
    .replaceAll("O", "0")
    .replaceAll("T", "7")
    .replaceAll("R", "2")
    .replaceAll("S", "5")
}
